package PerimeterTester.java;

public class PerimeterTester {
    private double height;
    private double wideth;
    PerimeterTester(double height,double wideth){
        this.height=height;
        this.wideth=wideth;
    }
    public double getHeight(){
        return height;
    }
    public double getWidth(){
        return wideth;
    }

}
